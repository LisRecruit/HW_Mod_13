package Dtos;

public class GeoDto{
        String lat;
        String lng;

    public void setLat(String lat) {
        this.lat = lat;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String lat() {
        return lat;
    }

    public String lng() {
        return lng;
    }
}
